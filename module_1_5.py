# 1. Создание переменной immutable_var с кортежем из разных типов данных
immutable_var = (
    "Hola mundo, 千里之行，始于足下!",  # строка
    888,               # целое число
    1013,2,          # число с плавающей запятой
    True,             # булевый тип
    (55, 66, 77),        # вложенный кортеж
    ["apple", "banana", "cherry"])  # список (изменяемый объект)
# 2. Вывод кортежа на экран
print("Кортеж immutable_var содержит следующие элементы:")
print(immutable_var)

# 3. Попытка изменить элементы кортежа
try:
    immutable_var[0] = "синхрофазатрон"  # Попытка изменить первый элемент
except TypeError as e:
    print("\nОшибка при попытке изменить элемент кортежа:")
    print(e)

# Объяснение, почему нельзя изменить значения элементов кортежа
print("\nКортежи в Python являются неизменяемыми (immutable) структурами данных.")
print("Это означает, что после создания кортежа его элементы нельзя изменить, "  
      "добавить или удалить. Это сделано для обеспечения целостности данных.")

# 4. Создание изменяемых структур данных
mutable_list = ["союз", "апполон", "меркурий"]  # Создание списка

# Изменение элементов списка
mutable_list[0] = "футбол"  # Изменяем первый элемент
mutable_list.append("тенис")  # Добавляем новый элемент

# Вывод измененного списка
print("\nИзмененный список mutable_list:")
print(mutable_list)